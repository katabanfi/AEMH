 <div  class="rolunk">     
    <div class="adye">A Nagyszentmiklósi út 9. szám alatt található régi kaszinó épülete az ötvenes évektől intézményesült
        formában látta el a művelődési ház szerepét.</div>
    <div class="ady">1997 - Új épületbe költözik a művelődési ház, méghozzá az egykori Szikra mozi helyére a Szegedi utca 13. szám alá.</div>
    <div class="ady">2003-tól a Kiss Mária Hortensia Honismereti Kör „otthonaként” szolgál a művelődési ház, és azóta is szorosan együttműködve tesszük színesebbé Kiszombor közművelődési életét.</div>
    <div class="ady">Falaink között több amatőr csoport működik. Ezek közül rendezvényeink állandó szereplői az
        Esőnap Álomszínház, illetve Dáridó Zombori módra.</div>
    <div class="ady">1990 óta folyamatosan megjelenik a Kiszombori Híradó című információs lap, melynek
    szerkesztősége szintén a művelődési ház falai közt működik. 2013 augusztusától megújult formában és új dizájnnal jelenik meg az újság.</div>
    <div class="ady">Művelődési házunk három fő helyiséggel szolgálja ki a település igényeit. Az első az aula, amely fogadások, zenés, táncos rendezvények lebonyolítását szolgálja, a második a színháztermünk, amely 2008-ban egy nagyobb korszerűsítésen esett át – új hang-és fénytechnikával lett felszerelve. A harmadik
    helyiségünk a klubhelyiség, melyet kisebb foglalkozásokra, kiállításokra használunk főként. Ezen felül hatalmas udvarral rendelkezünk, ahol rendszeresen kisebb-nagyobb rendezvényeinket valósítjuk meg. Természetesen nem csak művelődési házunk falai között szervezünk programokat, rengeteg szabadtéri
    és egyéb helyszínen tevékenykedve színesítjük Kiszombor művelődési életét.</div>

